		/// <summary>
		/// Gets the name of the given ISO 15924 enum value.
		/// </summary>
		/// <param name="script">ISO 15924 to look for its name.</param>
		/// <returns>Empty if the ISO 15924 value has not a name.</returns>
		public static string GetName (Iso15924 script) =>
			Iso15924Names.ContainsKey (script)
				? Iso15924Names[script]
				: string.Empty;

		/// <summary>
		/// Gets the code of the given ISO 15924 enum value.
		/// </summary>
		/// <param name="script">ISO 15924 to look for its code.</param>
		/// <returns>Empty if the ISO 15924 value has not a code.</returns>
		public static string GetCode (Iso15924 script) =>
			System.Enum.IsDefined (typeof (Iso15924), script)
				? script.ToString ().Substring (1).
					Insert (0, script.ToString ().Substring (0, 1))
				: string.Empty;

		/// <summary>
		/// Gets the ISO 15924 enum value for the given string code.
		/// </summary>
		/// <param name="code">String value of the code of an ISO 15924.</param>
		/// <returns><value>Iso15924.NONE</value> if a code is not found.
		/// </returns>
		public static Iso15924 GetIso15924 (string code) =>
			System.Enum.TryParse (code, true, out Iso15924 script)
				? script
				: Iso15924.NONE;

		/// <summary>
		/// Gets the direction of the given ISO 15924 enum value.
		/// </summary>
		/// <param name="script">ISO 15924 to look for its direction.</param>
		/// <returns><value>ScriptDirection.NONE</value> if the script
		/// is not found.</returns>
		public static ScriptDirection GetDirection (Iso15924 script) =>
			Iso15924Directions.ContainsKey (script)
				? Iso15924Directions[script]
				: ScriptDirection.NONE;

